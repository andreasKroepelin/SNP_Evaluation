    private void okBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okBActionPerformed
        ArrayList<Predicate<RowFilter.Entry<DefaultTableModel, Integer>>> predicates = new ArrayList<>();
        fps.stream().map(p -> p.getTester()).forEachOrdered(predicates::add);
        RowFilter<DefaultTableModel, Integer> rowfilter = new RowFilter<DefaultTableModel, Integer>() {
            @Override
            public boolean include(RowFilter.Entry<? extends DefaultTableModel, ? extends Integer> entry) {
                boolean ok = true;
                for (Predicate p : predicates) {
                    ok &= p.test(entry);
                }
                return ok;
            }
        };
        parent.setRowFilter(rowfilter);
        dispose();
    }//GEN-LAST:event_okBActionPerformed


