    public void loadFrom(ArrayList<String> descriptions) {
        fps.clear();
        filtersP.removeAll();
        for (String d : descriptions) {
            FilterPanel fp = new FilterPanel(parent.getTab(0).getColumnNames());
            fp.initFromDescription(d);
            add(fp);
        }
    }

    private void add(FilterPanel fp) {
        fps.add(fp);
        ((GridLayout) filtersP.getLayout()).setRows(fps.size());
        filtersP.add(fp);
        filtersP.validate();
        jPanel3.validate();
    }

    public ArrayList<String> getDescriptions() {
        ArrayList<String> des = new ArrayList<>();
        fps.stream().map(fp -> fp.getDescription()).forEachOrdered(des::add);
        return des;
    }

