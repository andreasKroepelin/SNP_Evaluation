    private void okBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okBActionPerformed
        ComparisonList cl = new ComparisonList();
        for (int i = 0; i < combobs.size(); i++) {
            JComboBox cb = combobs.get(i);
            switch (cb.getSelectedIndex()) {
                case 0:
                    cl.add(false, null, "");
                    break;
                case 1:
                    cl.add(true, (a, b) -> a - b, cb.getSelectedItem().toString() + ": " + parent.measures.get(i).name);
                    break;
                case 2:
                    cl.add(true, (a, b) -> b - a, cb.getSelectedItem().toString() + ": " + parent.measures.get(i).name);
                    break;
                case 3:
                    cl.add(true, (a, b) -> a / b, cb.getSelectedItem().toString() + ": " + parent.measures.get(i).name);
                    break;
                case 4:
                    cl.add(true, (a, b) -> b / a, cb.getSelectedItem().toString() + ": " + parent.measures.get(i).name);
                    break;
                default:
                    cl.add(false, null, "");
                    break;
            }

            parent.comparisons = cl;
            dispose();
        }
    }//GEN-LAST:event_okBActionPerformed


