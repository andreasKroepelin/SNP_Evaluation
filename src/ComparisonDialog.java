    public ComparisonDialog(Surface parent, boolean modal) {
        super(parent, modal);
        initComponents();

        this.parent = parent;

        String[] compFs = new String[]{
            "none",
            "difference (" + parent.vcfAlias1 + " - " + parent.vcfAlias2 + ")",
            "difference (" + parent.vcfAlias2 + " - " + parent.vcfAlias1 + ")",
            "ratio (" + parent.vcfAlias1 + " / " + parent.vcfAlias2 + ")",
            "ratio (" + parent.vcfAlias2 + " / " + parent.vcfAlias1 + ")"};

        compP.setLayout(new GridLayout(parent.measures.size(), 1));

        combobs = new ArrayList<>();

        for (Measure m : parent.measures) {
            JPanel p = new JPanel(new GridBagLayout());
            GridBagConstraints gridBagConstraints;
            JComboBox combo = new JComboBox();
            combo.setModel(new DefaultComboBoxModel(compFs));

            gridBagConstraints = new java.awt.GridBagConstraints();
            gridBagConstraints.gridx = 0;
            gridBagConstraints.gridy = 0;
            gridBagConstraints.anchor = java.awt.GridBagConstraints.WEST;
            gridBagConstraints.insets = new java.awt.Insets(3, 3, 0, 0);
            p.add(new JLabel(m.name + " using"), gridBagConstraints);

            gridBagConstraints = new java.awt.GridBagConstraints();
            gridBagConstraints.gridx = 1;
            gridBagConstraints.gridy = 0;
            gridBagConstraints.gridwidth = 2;
            gridBagConstraints.gridheight = 2;
            gridBagConstraints.fill = java.awt.GridBagConstraints.HORIZONTAL;
            gridBagConstraints.anchor = java.awt.GridBagConstraints.WEST;
            gridBagConstraints.weightx = 1.0;
            gridBagConstraints.insets = new java.awt.Insets(3, 3, 0, 0);
            p.add(combo, gridBagConstraints);

            combobs.add(combo);

            compP.add(p);

        }

        pack();
        setVisible(true);
    }

